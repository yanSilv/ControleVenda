    private void preecherTabela(ArrayList<ModVendedor> todosVendedores) {
        jTVendedores.getColumnModel().getColumn(0).setPreferredWidth(10);
        jTVendedores.getColumnModel().getColumn(1).setPreferredWidth(600);
        
        this.vend = todosVendedores; 
        
        DefaultTableModel modelo = (DefaultTableModel) jTVendedores.getModel();
        modelo.setNumRows(0);
        
        for (int i = 0; i < todosVendedores.size(); i++) {
            modelo.addRow(new Object[]{todosVendedores.get(i).getVend_id(), todosVendedores.get(i).getVend_nome()});
        }
    }
    
    private void mostraDados(ModVendedor get) {
        tfBairro.setText(get.getVend_bairro());
        tfCidade.setText(get.getVend_cidade());
        tfCodigoPesquisa.setText("");
        tfEndereco.setText(get.getVend_endereco());
        tfLogin.setText(get.getVend_login());
        tfNomePesquisa.setText("");
        tfNomeVendedor.setText(get.getVend_nome());
        tfSenha.setText(get.getVend_senha());
        tfTelefone.setText(get.getVend_telefone());
        tfid = get.getVend_id();
        jcEstado.setSelectedItem(get.getVend_estado());
        jcSexo.setSelectedItem(get.getVend_sex());
    }
    
