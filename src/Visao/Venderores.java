    private void jTVendedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTVendedoresMouseClicked
        int linhaTabela = jTVendedores.getSelectedRow();
        for(int i = 0; i < this.vend.size(); i++) {
            if ( (int) jTVendedores.getValueAt(linhaTabela, 0) == vend.get(i).getVend_id()) {
                mostraDados(vend.get(i));
                break;
            }    
        }
    }//GEN-LAST:event_jTVendedoresMouseClicked

    private void btPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisaActionPerformed
        ArrayList<ModVendedor> pesVen = new ArrayList<ModVendedor>();
        
        if (!tfCodigoPesquisa.getText().equals("") && tfNomePesquisa.getText().equals("")) {
            ven.setVend_id(Integer.parseInt(tfCodigoPesquisa.getText()));
            pesVen = ctr.pesquisaVendedor(ven, 1);
            
        } else if (tfCodigoPesquisa.getText().equals("") && !tfNomePesquisa.getText().equals("")) {
            ven.setVend_nome(tfNomePesquisa.getText());
            pesVen =  ctr.pesquisaVendedor(ven, 0);
            
        } else if (!tfCodigoPesquisa.getText().equals("") && !tfNomePesquisa.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Não é permitido a pesquisa com os dois campos preechidos");
        else
            JOptionPane.showMessageDialog(null, "É necessario o preenchimento de um dos dois campos da pesquisa");
        
        limpaCampos();
        preecherTabela(pesVen);
    }//GEN-LAST:event_btPesquisaActionPerformed


